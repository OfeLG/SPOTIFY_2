/*
Carpeta "shared"

La carpeta "shared" es donde colocas componentes, servicios y otros elementos que son compartidos en toda la 
aplicación. Estos son elementos que se utilizan en múltiples partes de tu aplicación, sin importar la 
característica o sección en la que te encuentres. Algunos ejemplos de lo que podría ir en la carpeta "shared" 
incluyen:

    - Componentes compartidos: Componentes reutilizables que se utilizan en diferentes partes de la aplicación. 
    Por ejemplo, un componente de barra de navegación o un componente de pie de página que aparecen en todas las 
    páginas de la aplicación.

    - Servicios compartidos: Servicios que proporcionan funcionalidad común a través de toda la aplicación, como 
    servicios de autenticación, servicios de notificación, servicios de manejo de datos compartidos, etc.

    - Directivas personalizadas: Directivas que se aplican a elementos en toda la aplicación. Por ejemplo, 
    una directiva que controla el enfoque de los elementos de entrada de datos en diferentes partes de la 
    aplicación.

    - Pipes personalizados: Pipes que realizan transformaciones comunes en datos y que son utilizados en múltiples 
    lugares de la aplicación.

*/