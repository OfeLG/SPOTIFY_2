/*
Services:

Propósito: La carpeta "services" se utiliza para almacenar servicios de Angular que proporcionan funcionalidad 
específica a tu aplicación.

Cuándo usarla: Debes usar la carpeta "services" para organizar y almacenar tus servicios Angular. Los 
servicios son componentes que pueden ser compartidos y reutilizados en diferentes partes de tu aplicación para 
realizar tareas específicas, como interactuar con API, gestionar datos, autenticar usuarios, etc.

Ejemplo: Puedes tener un servicio llamado "auth.service.ts" en la carpeta "services" que se encargue de la
autenticación de usuarios en toda la aplicación.



POR SI HAY DUDAS:

La principal diferencia entre servicios y microservicos radica en el alcance y el propósito de estas carpetas:

    - "services" se utiliza para servicios Angular que son reutilizables en toda la aplicación y están 
    relacionados con la lógica de negocio y la funcionalidad de la aplicación.

    - "microservices" se utiliza para organizar el código relacionado con los microservicios individuales de 
    una arquitectura de microservicios, donde cada microservicio puede tener su propia lógica y funcionalidad 
    independiente. Estos no son servicios de Angular, sino componentes más amplios de la arquitectura de 
    la aplicación
*/